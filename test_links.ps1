$okCount = 0
$failCount = 0
$results = @()

foreach ($item in $links) {
  try {
    $response = Invoke-WebRequest -Uri $item.url -Method Head -MaximumRedirection 5 -TimeoutSec 10 -UseBasicParsing -ErrorAction Stop
    $status = $response.StatusCode
    if ($status -eq 200) {
      $statusText = "OK"
      $okCount++
    } else {
      $statusText = "WARN ($status)"
      $failCount++
    }
  } catch {
    $statusCode = $_.Exception.Response.StatusCode.value__
    if ($statusCode) {
      $statusText = "FAIL ($statusCode)"
    } else {
      $statusText = "FAIL (connection error)"
    }
    $failCount++
  }
  $result = "$($item.id) | $($item.name) | $statusText"
  Write-Host $result
  $results += $result
}

Write-Host ""
Write-Host "===== SUMMARY ====="
Write-Host "Total: $($links.Count)"
Write-Host "OK: $okCount"
Write-Host "FAIL: $failCount"
